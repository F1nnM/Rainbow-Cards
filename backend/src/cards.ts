export function getShuffledStack(allSets: any, setsToUse: string[]) {
    let cards = []
    for (let set of setsToUse) {
        if (allSets[set])
            for (let text of allSets[set].cards)
                cards.push({text, mark: allSets[set].mark})
    }
    shuffleArrayInplace(cards)
    return cards
}

